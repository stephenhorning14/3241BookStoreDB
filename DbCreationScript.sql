CREATE TABLE IF NOT EXISTS "AuthoredBooks" (
	"AuthorNum"	INTEGER NOT NULL,
	"ItemNum"	INTEGER NOT NULL,
	FOREIGN KEY("AuthorNum") REFERENCES "Author"("AuthorNum"),
	FOREIGN KEY("ItemNum") REFERENCES "Book"("ItemNum"),
	PRIMARY KEY("AuthorNum","ItemNum")
);
CREATE TABLE IF NOT EXISTS "Book" (
	"Title"	TEXT NOT NULL,
	"Genre"	TEXT NOT NULL,
	"ReleaseYear"	TEXT NOT NULL,
	"ItemNum"	INTEGER NOT NULL,
	"Publisher"	TEXT NOT NULL,
	"ISBN"	TEXT,
	FOREIGN KEY("ItemNum") REFERENCES "Item"("ItemNum"),
	PRIMARY KEY("ItemNum"),
	FOREIGN KEY("Publisher") REFERENCES "Publisher"("Name")
);
CREATE TABLE IF NOT EXISTS "Customer" (
	"AccountNum"	INTEGER NOT NULL,
	"Name"	TEXT NOT NULL,
	"Birthday"	TEXT NOT NULL,
	"Address"	TEXT NOT NULL,
	PRIMARY KEY("AccountNum")
);
CREATE TABLE IF NOT EXISTS "CustEmail" (
	"AccountNum"	INTEGER NOT NULL,
	"EmailAddress"	TEXT NOT NULL,
	PRIMARY KEY("AccountNum","EmailAddress")
);
CREATE TABLE IF NOT EXISTS "ItemISBN" (
	"ISBN"	TEXT NOT NULL,
	"ItemNum"	INTEGER NOT NULL,
	PRIMARY KEY("ISBN","ItemNum")
);
CREATE TABLE IF NOT EXISTS "Employee" (
	"EmployeeNum"	INTEGER NOT NULL,
	"Address"	TEXT NOT NULL,
	"Name"	TEXT NOT NULL,
	PRIMARY KEY("EmployeeNum")
);
CREATE TABLE IF NOT EXISTS "Item" (
	"ItemNum"	INTEGER NOT NULL,
	"SellPrice"	REAL NOT NULL,
	"PurchasedPrice"	REAL NOT NULL,
	"Quantity"	INTEGER NOT NULL,
	PRIMARY KEY("ItemNum")
);
CREATE TABLE IF NOT EXISTS "Purchase" (
	"PurchaseNum"	INTEGER NOT NULL,
	"Date"	TEXT NOT NULL,
	"TotalPrice"	REAL NOT NULL,
	"PaymentMethod"	TEXT NOT NULL,
	"SoldBy"	INTEGER NOT NULL,
	"SoldTo"	INTEGER NOT NULL,
	FOREIGN KEY("SoldTo") REFERENCES "Customer"("AccountNum"),
	FOREIGN KEY("SoldBy") REFERENCES "Employee"("EmployeeNum"),
	PRIMARY KEY("PurchaseNum")
);
CREATE TABLE IF NOT EXISTS "SuppliedItems" (
	"ItemNum"	INTEGER NOT NULL,
	"SupplierNum"	INTEGER NOT NULL,
	FOREIGN KEY("ItemNum") REFERENCES "Item"("ItemNum"),
	FOREIGN KEY("SupplierNum") REFERENCES "Supplier"("SupplierNum"),
	PRIMARY KEY("ItemNum","SupplierNum")
);
CREATE TABLE IF NOT EXISTS "Author" (
	"AuthorNum"	INTEGER NOT NULL,
	"Name"	TEXT NOT NULL,
	PRIMARY KEY("AuthorNum")
);
CREATE TABLE IF NOT EXISTS "DVD" (
	"ItemNum"	INTEGER NOT NULL,
	"Director"	TEXT NOT NULL,
	"ReleaseDate"	TEXT NOT NULL,
	"Title"	TEXT NOT NULL,
	"Format"	TEXT NOT NULL,
	"Genre"	TEXT NOT NULL,
	PRIMARY KEY("ItemNum"),
	FOREIGN KEY("ItemNum") REFERENCES "Item"("ItemNum")
);
CREATE TABLE IF NOT EXISTS "Dummy" (
	"ISBN"	TEXT,
	"TITLE"	TEXT,
	"AUTHOR"	TEXT,
	"PUBLISHER"	TEXT,
	"YEAR"	TEXT,
	"PRICE"	TEXT,
	"GENRE"	TEXT
);
CREATE TABLE IF NOT EXISTS "PurchasedItems" (
	"ItemNum"	INTEGER NOT NULL,
	"PurchaseNum"	INTEGER NOT NULL,
	"QuantityPurchased"	INTEGER NOT NULL DEFAULT 1,
	PRIMARY KEY("ItemNum","PurchaseNum"),
	FOREIGN KEY("ItemNum") REFERENCES "Item"("ItemNum"),
	FOREIGN KEY("PurchaseNum") REFERENCES "Purchase"("PurchaseNum")
);
CREATE TABLE IF NOT EXISTS "CD" (
	"ItemNum"	INTEGER NOT NULL,
	"Artist"	TEXT NOT NULL,
	"Genre"	TEXT NOT NULL,
	"ReleaseDate"	TEXT NOT NULL,
	"Title"	TEXT,
	PRIMARY KEY("ItemNum"),
	FOREIGN KEY("ItemNum") REFERENCES "Item"("ItemNum")
);
CREATE TABLE IF NOT EXISTS "Supplier" (
	"SupplierNum"	INTEGER NOT NULL,
	"Name"	TEXT NOT NULL,
	"Address"	TEXT NOT NULL,
	PRIMARY KEY("SupplierNum")
);
CREATE TABLE IF NOT EXISTS "Publisher" (
	"Name"	TEXT NOT NULL,
	"Address"	TEXT NOT NULL,
	PRIMARY KEY("Name")
);
